package TDD.ROT13.Solucion;

public class Main {

    //LOS TESTS ESTAN EN Ejercicios/test/java/
    String rot13(String s) {
        String result = "";
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);
            if (c >= 'a' && c <= 'm') {
                c += 13;
            } else if (c >= 'A' && c <= 'M') {
                c += 13;
            } else if (c >= 'n' && c <= 'z') {
                c -= 13;
            } else if (c >= 'N' && c <= 'Z') {
                c -= 13;
            }
            result += c;
        }
        return result;
    }

}
